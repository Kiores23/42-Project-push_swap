#include "push_swap_utils.h"

long	ft_atoi(const char *str)
{
	char	*s;
	long	r;
	int		sign;

	s = (char *)str;
	sign = 1;
	r = 0;
	while ((9 <= *s && *s <= 13) || *s == 32)
		s++;
	if (*(s) == '+' || *s == '-')
	{
		if (*(s++) == '-')
			sign *= -1;
	}
	s--;
	while (ft_isdigit(*(++s)) && r <= 2147483648)
		r = r * 10 + *s - '0';
	return (r * sign);
}
